#include "push_swap.h"

int	isalldigit(char *s)
{
	int	i;

	i = 0;
	while (s[i])
	{
		if (!ft_isdigit(s[i]) && !(s[i] == ' '))
			return (0);
		i++;
	}
	return (1);
}
